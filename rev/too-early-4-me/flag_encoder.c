void encode_string(char* plaintext) {
	unsigned char output[strlen(plaintext) + 1];
	for (int i = 0; i < strlen(plaintext); i++){
		output[i] = sbox[(unsigned char)plaintext[i]];
	}

	output[strlen(plaintext)] = '\0';
	// printf("int flag_len = %ld;\n", strlen(plaintext));
	printf("unsigned char encoded_flag[] = {");
	for(int i = 0; i < strlen(plaintext); i++) {
		printf("0x%02x", output[i]);
		if(i+1 != strlen(plaintext)) {
			printf("%s", ", ");
		}
	}
	printf("%s", "};\n");
}

int main(int argc, char** argv) {
	if (argc != 2) {
		fprintf(stderr, "\tUSAGE: %s <string_to_encode>\n", argv[0]);
		return 1;
	}

	encode_string(argv[1]);
	return 0;
}
